                <p>• It is always a small thought but a big dream that manifested itself into greater action and we
                   remain grateful to, Mother Veronica who founded the Apostolic Carmel Congregation in 1868
                   in France with great vision and instilling it in Indian soil in 1870 with St Ann’s convent in
                   Mangalore. Since then, the Apostolic Carmel Sisters are steering the institutions to ever greater
                   heights.
                   In 1921, Mother Aloysia A.C. prologued her vision for imparting excellent women education
                   which translated into the formation of St Ann’s college and soon turned to what is now known
                   as St Agnes College. This was the first women college in the west coast of India as well as the
                   first catholic women college in the country. The college is known for its quality and dedicated
                   service in the education sector for 102 years.
                   Let me mention a few milestones the college has witnessed in the past 100 years.<br>
                    • In 1999, the college in the accreditation process by NAAC received 5 stars and in the
                      year 2005 and 2012 the college earned an ‘A grade’.<br>
                    • St Agnes college has also been recognized by UGC as “College of excellence”, being
                      the first college under mangalore university to receive this recognition.<br>
                    • In 2017, the college secured A+ grade with CGPA 3.65 on 4.              
                      The college is successful in achieving good character formation, social concern and academic
                      excellence and continues to be the guiding ethos of the Agnesians along with the vision of
                      building a secular democratic and inclusive society.</p>
